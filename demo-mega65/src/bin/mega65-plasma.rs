static mut PRNG: u32 = 1;

fn rand8() -> u8 {
    unsafe {
        PRNG ^= PRNG << 13;
        PRNG ^= PRNG >> 17;
        PRNG ^= PRNG << 5;
        PRNG as u8
    }
}

static mut Y_PHASE1: u8 = 0;
static mut Y_PHASE2: u8 = 0;
static mut X_PHASE1: u8 = 0;
static mut X_PHASE2: u8 = 0;

fn generate_charset() {
    let bits = [1u8, 2, 4, 8, 16, 32, 64, 128];
    for (c, &threshold) in SINE_TABLE.iter().enumerate() {
        for row in 0..8 {
            let mut pattern: u8 = 0;
            for &bit in &bits {
                if rand8() > threshold {
                    pattern |= bit;
                }
            }
            unsafe {
                ptr::write_volatile((CHARSET_ADDRESS as usize + c * 8 + row) as *mut u8, pattern)
            }
        }
    }
}

fn unlock_vic4() {
    unsafe {
        ptr::write_volatile(0xD02F as *mut u8, 0x47);
        ptr::write_volatile(0xD02F as *mut u8, 0x53);
    }
}

fn speed_mode3() {
    unsafe {
        let ctrlb = ptr::read_volatile(0xD031 as *const u8);
        ptr::write_volatile(0xD031 as *mut u8, ctrlb | 0x40);
        let ctrlc = ptr::read_volatile(0xD054 as *const u8);
        ptr::write_volatile(0xD054 as *mut u8, ctrlc & !0x40);
    }
}

fn draw() {
    let screen = SCREEN_ADDRESS as *mut u8;
    unsafe {
        let mut ybuf: [u8; ROWS] = [0; ROWS];
        let mut xbuf: [u8; COLS] = [0; COLS];

        let mut ya = Y_PHASE1;
        let mut yb = Y_PHASE2;
        for y in ybuf.iter_mut() {
            *y = SINE_TABLE[ya as usize].wrapping_add(SINE_TABLE[yb as usize]);
            ya = ya.wrapping_add(4);
            yb = yb.wrapping_add(9);
        }
        Y_PHASE1 = Y_PHASE1.wrapping_add(3);
        Y_PHASE2 = Y_PHASE2.wrapping_sub(5);

        let mut xa = X_PHASE1;
        let mut xb = X_PHASE2;
        for x in xbuf.iter_mut() {
            *x = SINE_TABLE[xa as usize].wrapping_add(SINE_TABLE[xb as usize]);
            xa = xa.wrapping_add(3);
            xb = xb.wrapping_add(7);
        }
        X_PHASE1 = X_PHASE1.wrapping_add(2);
        X_PHASE2 = X_PHASE2.wrapping_sub(3);

        for (row, &y) in ybuf.iter().enumerate() {
            for (col, &x) in xbuf.iter().enumerate() {
                ptr::write_volatile(screen.add(row * COLS + col), x.wrapping_add(y))
            }
        }
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn main() -> ! {
    unlock_vic4();
    generate_charset();

    unsafe {
        ptr::write_volatile(0xD068 as *mut u8, CHARSET_ADDRESS as u8);
        ptr::write_volatile(0xD069 as *mut u8, (CHARSET_ADDRESS >> 8) as u8);
        ptr::write_volatile(0xD06A as *mut u8, (CHARSET_ADDRESS >> 16) as u8);
    }

    speed_mode3();

    loop {
        draw();
    }
}
