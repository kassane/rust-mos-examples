const VIC: usize = 0xD000;
const VIC_BORDER: usize = VIC + 0x020;
const VIC_MEM: usize = VIC + 0x018;
const SID_V3_FREQ_LO: usize = VIC + 0x40E;
const SID_V3_FREQ_HI: usize = VIC + 0x40F;
const SID_V3_CTRL: usize = VIC + 0x412;
const SID_OSC3: usize = VIC + 0x41B;
const SCREEN: usize = 0x0400;
const CHARSET: usize = 0x2000;

fn poke(addr: usize, val: u8) {
    unsafe { (addr as *mut u8).write_volatile(val) }
}

fn peek(addr: usize) -> u8 {
    unsafe { (addr as *mut u8).read_volatile() }
}

