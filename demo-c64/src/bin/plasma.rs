fn start_sid_random() {
    poke(SID_V3_FREQ_LO, 0xFF);
    poke(SID_V3_FREQ_HI, 0xFF);
    poke(SID_V3_CTRL, 0x80);
}

fn rand8() -> u8 {
    peek(SID_OSC3)
}

fn make_charset() {
    for c in 0..256u16 {
        let threshold = SINE_TABLE[c as usize];
        for row in 0..8u16 {
            let mut pattern: u8 = 0;
            if rand8() > threshold {
                pattern |= 0x01;
            }
            if rand8() > threshold {
                pattern |= 0x02;
            }
            if rand8() > threshold {
                pattern |= 0x04;
            }
            if rand8() > threshold {
                pattern |= 0x08;
            }
            if rand8() > threshold {
                pattern |= 0x10;
            }
            if rand8() > threshold {
                pattern |= 0x20;
            }
            if rand8() > threshold {
                pattern |= 0x40;
            }
            if rand8() > threshold {
                pattern |= 0x80;
            }
            poke(CHARSET + (c as usize) * 8 + row as usize, pattern);
        }
        poke(VIC_BORDER, peek(VIC_BORDER).wrapping_add(1));
    }
}

fn do_plasma() -> ! {
    let mut x_cnt1: u8 = 0;
    let mut x_cnt2: u8 = 0;
    let mut y_cnt1: u8 = 0;
    let mut y_cnt2: u8 = 0;
    let screen = SCREEN as *mut u8;

    loop {
        let mut yk = y_cnt1;
        let mut yl = y_cnt2;
        let mut ydata = [0u8; 25];
        for y in &mut ydata {
            *y = SINE_TABLE[yk as usize].wrapping_add(SINE_TABLE[yl as usize]);
            yk = yk.wrapping_add(4);
            yl = yl.wrapping_add(9);
        }
        y_cnt1 = y_cnt1.wrapping_add(3);
        y_cnt2 = y_cnt2.wrapping_sub(5);

        let mut xk = x_cnt1;
        let mut xl = x_cnt2;
        let mut xdata = [0u8; 40];
        for x in &mut xdata {
            *x = SINE_TABLE[xk as usize].wrapping_add(SINE_TABLE[xl as usize]);
            xk = xk.wrapping_add(3);
            xl = xl.wrapping_add(7);
        }
        x_cnt1 = x_cnt1.wrapping_add(2);
        x_cnt2 = x_cnt2.wrapping_sub(3);

        for (row, &y) in ydata.iter().enumerate() {
            for (col, &x) in xdata.iter().enumerate() {
                unsafe {
                    screen.add(row * 40 + col).write_volatile(y.wrapping_add(x));
                }
            }
        }
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn main() -> ! {
    start_sid_random();
    make_charset();
    poke(
        VIC_MEM,
        ((SCREEN >> 6) & 0xF0) as u8 | ((CHARSET >> 10) & 0x0E) as u8,
    );
    do_plasma();
}
